

class ExpensePayment:

  def __init__(expense_amount,date,reason):
    self.expense_amount = expense_amount
    self.date = date
    self.reason = reason

  def get_expense_amount():
    return self.expense_amount

  def get_expense_date():
    return self.date

  def get_reason():
    return self.reason

  def set_reason(reason):
    self.reason = reason

  def set_expense_amount(expense_amount):
    self.expense_amount = expense_amount


  
